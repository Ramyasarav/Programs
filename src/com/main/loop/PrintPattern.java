package com.main.loop;

public class PrintPattern {
	int i,j;

	public static void main(String[] args) {
		
		for (int i=0;i <= 5; i++){
			System.out.println();
			
			for (int j= 0;j <= i;j++){
			
						System.out.print("*");
						
			
			}
		}
				
				
				
	}
}

				
				
				
				
				
				